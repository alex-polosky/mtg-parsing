def _add_to_list(l):
    add = []
    for _t in l:
        if _t[-1] == 'f':
            add.append(f'{_t[:-1]}ves')
        elif _t[-1] == 's':
            add.append(f'{_t}es')
        elif _t[-1] == 'y':
            add.append(f'{_t[:-1]}ies')
        else:
            add.append(f'{_t}s')
    l += add

